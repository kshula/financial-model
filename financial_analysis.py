def calculate_profitability_ratio(profit, revenue):
    """
    Calculate the profitability ratio (profit margin) of a business.
    """
    profitability_ratio = (profit / revenue) * 100
    return profitability_ratio

def calculate_return_on_investment(gain, cost):
    """
    Calculate the Return on Investment (ROI) of a project.
    """
    roi = (gain - cost) / cost * 100
    return roi

def calculate_debt_to_equity_ratio(total_debt, total_equity):
    """
    Calculate the Debt-to-Equity ratio of a company.
    """
    debt_to_equity_ratio = total_debt / total_equity
    return debt_to_equity_ratio
